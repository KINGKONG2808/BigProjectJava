    public void showTable() {
        listProducts = ShowProducts.getListProducts();
        int i = 1;
        for (Products listPro : listProducts) {
            model.addRow(new Object[]{
                i++, listPro.getMaHang(), listPro.getTenHang(), listPro.getGia(), listPro.getNhaCungCap(), listPro.getSoLuong()
            });
        }
    }

