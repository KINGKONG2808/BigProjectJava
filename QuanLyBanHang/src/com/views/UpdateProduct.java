    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        Products product = new Products();
        if (txtGia.getText().equals("") || txtMaHang.getText().equals("") || txtNhaCungCap.getText().equals("") 
                || txtSoLuong.getText().equals("") || txtTenHang.getText().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Mời nhập đây đủ thông tin!!!");
        }else{
        try {
           // Products product = new Products();
            product.setMaHang(Integer.parseInt(txtMaHang.getText()));
            product.setTenHang(txtTenHang.getText());
            product.setGia(Float.parseFloat(txtGia.getText()));
            product.setNhaCungCap(txtNhaCungCap.getText());
            product.setSoLuong(Integer.parseInt(txtSoLuong.getText()));

            if (UpdateOneProduct.update(product)) {
                JOptionPane.showMessageDialog(rootPane, "Cập nhật thành công!!!");
                model.setRowCount(0);
                showTable();
            } else {
                JOptionPane.showMessageDialog(rootPane, "Mã hàng không tồn tại!!!");
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }


    }//GEN-LAST:event_btnUpdateActionPerformed

